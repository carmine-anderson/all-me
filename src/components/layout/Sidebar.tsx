export function Sidebar() {
  const { sidebarOpen, setSidebarOpen } = useUIStore()

  return (
    <>
      {/* Mobile overlay */}
      <AnimatePresence>
        {sidebarOpen && (
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            className="fixed inset-0 z-20 bg-black/50 md:hidden"
            onClick={() => setSidebarOpen(false)}
          />
        )}
      </AnimatePresence>

      {/* Sidebar */}
      <motion.aside
        initial={false}
        animate={{ width: sidebarOpen ? 220 : 0, opacity: sidebarOpen ? 1 : 0 }}
        transition={{ duration: 0.25, ease: 'easeInOut' }}
        className={cn(
          'fixed left-0 top-0 z-30 flex h-full flex-col overflow-hidden border-r border-surface-border bg-surface-card md:relative md:z-auto',
          !sidebarOpen && 'pointer-events-none'
        )}
      >
        <div className="flex h-16 items-center px-5">
          <span className="text-gradient text-xl font-bold tracking-tight">AllMe</span>
        </div>

        <nav className="flex-1 space-y-1 px-3 py-2">
          {navItems.map((item) => (
            <NavLink
              key={item.to}
              to={item.to}
              onClick={() => {
                // Close sidebar on mobile after navigation
                if (window.innerWidth < 768) setSidebarOpen(false)
              }}
              className={({ isActive }) =>
                cn(
                  'flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-all duration-150',
                  isActive
                    ? 'bg-brand-500/10 text-brand-400'
                    : 'text-zinc-400 hover:bg-surface-elevated hover:text-zinc-100'
                )
              }
            >
              {item.icon}
              <span className="whitespace-nowrap">{item.label}</span>
            </NavLink>
          ))}
        </nav>

        <div className="border-t border-surface-border p-3">
          <p className="px-3 text-xs text-zinc-600">AllMe v0.1.0</p>
        </div>
      </motion.aside>
    </>
  )
}
